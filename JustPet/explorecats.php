                                 <h3>Persian</h3>
                                 <div class="text">
                                            <br><br><br><br><br>
                                   <b>Also lovingly referred to as one of the "smushed-face" cats, Persians have beautiful, long, fur coats. They can come in almost any color, depending on which cat registry association you are looking at, and have a flat face when compared to most other breeds of cats. Some breed descriptions even state the nose is not allowed to protrude past the eyes when viewed in a profile.
Persians have been seen in many movies, art, commercials, and homes throughout the years and have also been one of the most popular breeds of cats for decades.
Their coats demand regular grooming, otherwise, matting will result. Like many purebreds, they are prone to a variety of diseases, including renal and cardiac problems.Persians are also known as the Persian Longhair, the Iranian cat, and the Shirazi cat, named after the city of Shiraz, Iran, since they were originally imported from Iran.</div>
